# Q1. Write a program to Input two numbers and Print their addition, subtraction, multiplication ,division and modulus.

a = float(input("Enter first number: "))
b = float(input("Enter second number: "))

print("Addition : ", a+b)
print("Subtraction : ", a-b)
print("Multiplication : ", a*b)
print("Division : ", a/b)
print("Modulus : ", a%b)
